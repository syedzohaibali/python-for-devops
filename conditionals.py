day = input("Enter the day of the week: ").lower()
if day == "monday":
    print("Start of the work week!")    
elif day == "tuesday":
    print("It's Tuesday, keep going!")
elif day == "wednesday":
    print("Midweek already!")
elif day == "thursday":
    print("Almost the weekend!")
elif day == "friday":
    print("It's Friday, the weekend is near!")
elif day == "saturday":
    print("It's Saturday, enjoy your day!")
elif day == "sunday":
    print("It's Sunday, rest well!")
    